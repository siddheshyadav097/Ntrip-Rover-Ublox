/**
  * @brief  Conversion complete callback in non blocking mode
  * @param  AdcHandle : AdcHandle handle
  * @note   This example shows a simple way to report end of conversion
  *         and get conversion result. You can add your own implementation.
  * @retval None
  */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *AdcHandle)
{
     if(AdcHandle->Instance == ADC1)
    {
      /* Report to sensor_api.c program that ADC sequencer has reached its end */
       sensorScanCompleted = SET;
    }
    if(AdcHandle->Instance == ADC2)   //if supply sense addc callback is generated
    {
       /* Report to bat_api.c program that ADC sequencer has reached its end */
       ubSequenceCompleted = SET;
    }
    
}

